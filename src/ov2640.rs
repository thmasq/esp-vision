#![allow(dead_code)]

use embedded_hal_async::i2c::I2c;

const OV2640_ADDR: u8 = 0x60 >> 1;

#[derive(Debug, Clone, Copy)]
pub enum Resolution {
    Res160x120,
    Res320x240,
    Res640x480,
    Res800x600,
}

#[derive(Debug)]
pub enum Error<I2cErr> {
    I2c(I2cErr),
}

pub struct Ov2640<I2C> {
    i2c: I2C,
}

impl<I2C, I2cErr> Ov2640<I2C>
where
    I2C: I2c<Error = I2cErr>,
{
    pub fn new(i2c: I2C) -> Self {
        Self { i2c }
    }

    pub async fn init_jpeg(&mut self) -> Result<(), Error<I2cErr>> {
        self.write_reg(0xFF, 0x01).await?;
        self.write_reg(0x12, 0x80).await?;

        self.write_regs(OV2640_JPEG_INIT).await?;
        self.write_regs(OV2640_YUV422).await?;

        self.write_reg(0xFF, 0x01).await?;
        self.write_reg(0x15, 0x00).await?;

        Ok(())
    }

    pub async fn init_yuv422(&mut self) -> Result<(), Error<I2cErr>> {
        self.write_reg(0xFF, 0x01).await?;
        self.write_reg(0x12, 0x80).await?;

        self.write_regs(OV2640_YUV422_INIT).await?;

        self.write_reg(0xFF, 0x00).await?;
        self.write_reg(0xDA, 0x10).await?;

        Ok(())
    }

    pub async fn set_resolution(&mut self, res: Resolution) -> Result<(), Error<I2cErr>> {
        let regs = match res {
            Resolution::Res160x120 => OV2640_160X120_JPEG,
            Resolution::Res320x240 => OV2640_320X240_JPEG,
            Resolution::Res640x480 => OV2640_640X480_JPEG,
            Resolution::Res800x600 => OV2640_800X600_JPEG,
        };
        self.write_regs(regs).await?;
        Ok(())
    }

    async fn write_regs(&mut self, regs: &[(u8, u8)]) -> Result<(), Error<I2cErr>> {
        for &(reg, val) in regs {
            self.write_reg(reg, val).await?;
        }
        Ok(())
    }

    async fn write_reg(&mut self, reg: u8, val: u8) -> Result<(), Error<I2cErr>> {
        self.i2c
            .write(OV2640_ADDR, &[reg, val])
            .await
            .map_err(Error::I2c)
    }
}

/// Utility function to compress a YUYV (YUV422) buffer in-place to a Y (monochrome) buffer.
/// It slides all the Y bytes to the front of the slice.
/// Returns the length of the new compacted monochrome data (exactly half of the original length).
pub fn compact_yuyv_to_mono_in_place(buffer: &mut [u8]) -> usize {
    let pixel_count = buffer.len() / 2;

    for (i, read_idx) in (0..buffer.len()).step_by(4).enumerate() {
        let write_idx = i * 2;

        buffer[write_idx] = buffer[read_idx]; // Y0
        buffer[write_idx + 1] = buffer[read_idx + 2]; // Y1
    }

    pixel_count
}

